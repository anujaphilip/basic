import java.awt.*;
public class Framedemo
{
 public static void main(String args[])
 {
   Frame f=new Frame("Label Demo");
   f.setSize(200,100);
   f.setLayout(new FlowLayout());
   Label l=new Label("Hello World");
   f.add(l);
   Button b=new Button("Exit");
   f.add(b);
   f.setVisible(true);
 }
}
